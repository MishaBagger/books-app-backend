function generateSlug(title) {
    const cyrillicMap = {
      'а': 'a', 'А': 'A',
      'б': 'b', 'Б': 'B',
      'в': 'v', 'В': 'V',
      'г': 'g', 'Г': 'G',
      'д': 'd', 'Д': 'D',
      'е': 'e', 'Е': 'E',
      'ё': 'yo', 'Ё': 'Yo',
      'ж': 'zh', 'Ж': 'Zh',
      'з': 'z', 'З': 'Z',
      'и': 'i', 'И': 'I',
      'й': 'y', 'Й': 'Y',
      'к': 'k', 'К': 'K',
      'л': 'l', 'Л': 'L',
      'м': 'm', 'М': 'M',
      'н': 'n', 'Н': 'N',
      'о': 'o', 'О': 'O',
      'п': 'p', 'П': 'P',
      'р': 'r', 'Р': 'R',
      'с': 's', 'С': 'S',
      'т': 't', 'Т': 'T',
      'у': 'u', 'У': 'U',
      'ф': 'f', 'Ф': 'F',
      'х': 'h', 'Х': 'H',
      'ц': 'ts', 'Ц': 'Ts',
      'ч': 'ch', 'Ч': 'Ch',
      'ш': 'sh', 'Ш': 'Sh',
      'щ': 'sch', 'Щ': 'Sch',
      'ъ': '', 'Ъ': '',
      'ы': 'y', 'Ы': 'Y',
      'ь': '', 'Ь': '',
      'э': 'e', 'Э': 'E',
      'ю': 'yu', 'Ю': 'Yu',
      'я': 'ya', 'Я': 'Ya'
  };
  
    return title
      .toString()
      .toLowerCase()
      .split('')
      .map(char => cyrillicMap[char] || char) // Заменяем русские буквы
      .join('')
      .normalize('NFD') // Разбиваем акцентированные символы
      .replace(/[\u0300-\u036f]/g, '') // Удаляем диакритические знаки
      .replace(/[^\w\s-]/g, '') // Удаляем оставшиеся не-словесные символы
      .replace(/\s+/g, '-') // Заменяем пробелы на дефисы
      .replace(/-+/g, '-') // Убираем двойные дефисы
      .trim()
      .substring(0, 100);
  }

module.exports = generateSlug;